    public boolean checkTeacherID(String tID) throws ClassNotFoundException
    {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkUser = false;
        String query = "SELECT * FROM Teacher WHERE TeacherId=?";
        
        try {
              Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager
            .getConnection(
              "jdbc:sqlserver://localhost:1433;databaseName=SavetheFuture;selectMethod=cursor", "sa", "123456");
           
             ps=connection.prepareStatement(query);
            ps.setString(1, tID);
            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                checkUser = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(teacherRegForm.class.getName()).log(Level.SEVERE, null, ex);
        }
         return checkUser;
    }

    
