    private void Jbutton_Register_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbutton_Register_ActionPerformed
        // TODO add your handling code here:

        String tfname = jTextField_tFirstName.getText();
        String tlname = jTextField_tLastName.getText();
        String tdes = jTextField_tdes.getText();
        String tphn = jTextField_tPhone.getText();
        String tID = jTextField_tID.getText();
        String temail = jTextField_tEmail.getText();

        String pass = String.valueOf(jPasswordField_PASS.getPassword());

        String re_pass = String.valueOf(jPasswordField_REPASS.getPassword());

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager
            .getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=SavetheFuture;selectMethod=cursor", "sa", "123456");
            
            String query = "INSERT INTO Teacher(TeacherId, Teacher_FirstName, Teacher_LastName , Teacher_Designation,Teacher_Email,password,Teacher_PhoneNo )VALUES (?,?,?,?,?,?,?)";
            PreparedStatement pst=connection.prepareStatement(query);

            pst.setString(1, tID);
            pst.setString(2, tfname);
            pst.setString(3, tlname);
            pst.setString(4, tdes);
            pst.setString(5, temail);
            pst.setString(6, pass);
            
            pst.setString(7, tphn);

            if(tID.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Add Teacher ID");
            }

            else if(pass.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Add A Password");
            }
            else if(!pass.equals(re_pass))
            {
                JOptionPane.showMessageDialog(null, "Retype The Password Again");
            }
            else if(checkTeacherID(tID))
            {
                JOptionPane.showMessageDialog(null, "This Studemt Already Exists");
            }
            if(pst.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "New Teacher Add");
            }

        } catch (SQLException ex) {
            Logger.getLogger(teacherRegForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(teacherRegForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Jbutton_Register_ActionPerformed

